    ### CLASS VARIABLES ###

    __documentation_section__ = 'Specifiers'

    __slots__ = (
        '_color',
        '_selector',
        )

    ### INITIALIZER ###

    def __init__(
        self,
        color=None,
        selector=None,
        ):
        if color is not None:
            assert isinstance(color, (list, str)), repr(color)
        self._color = color
        if selector is not None:
            assert isinstance(selector, abjad.Selector)
        self._selector = selector

    ### SPECIAL METHODS ###

    def __call__(self, argument=None):
        r'''Calls specifier on `argument`.

        Returns none.
        '''
        if not argument:
            return
        if not self.color:
            return
        if isinstance(self.color, str):
            colors = [self.color]
        elif isinstance(self.color, list):
            colors = self.color
        else:
            raise TypeError(self.color)
        selector = self.selector or baca.select_leaves()
        #print(format(selector))
        #print()
        selections = selector(argument)
        #print(selections)
        selections = baca.MusicMaker._normalize_selections(selections)
        selections = abjad.select(selections)
        abjad.label(selections).color_alternating(colors)

    ### PUBLIC PROPERTIES ###

    @property
    def color(self):
        r'''Gets color.

        Defaults to none.

        Set to string or none.

        Returns string or none.
        '''
        return self._color

    @property
    def selector(self):
        r'''Gets selector.

        Defaults to none.

        Set to selector or none.

        Returns selector or none.
        '''
        return self._selector
